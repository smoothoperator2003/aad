# Practical 9 create a simple html hello world project using angularjs framework and applyng controller ng model and expressions


# check whether node.js is downloaded or not
npm --version

# install angular cli globally
npm install -g @angular/cli

# create angular app
ng new my-app

# to run scripts
Set-ExecutionPolicy -Scope CurrentUser -ExecutionPolicy RemoteSigned

cd my-app

# Angular CLI compiles the application, starts a development server, and watches for changes to your files.
ng serve

# run the app 
angular app -> http://localhost:4200/
